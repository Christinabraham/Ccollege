                Welcome to Ewing Christian College, Allahabad, a prestigious Autonomous Minority Institution, a reputed Centre of Learning and a Constituent College of the University of Allahabad. Ewing Christian College is one of the best undergraduate colleges in this part of the country, having earned great reputation in all spheres of academics. Ewing Christian College was also amongst the first colleges in the country to be selected for College Science Improvement Programme (COSIP) started by UGC in the 1970’s.

Ewing Christian College was granted Autonomy in 1994 and was again amongst the first batch of 35 colleges in the country where the UGC introduced vocational courses under its scheme for vocationalisation of Degree courses. The college further consolidated its position as an institution imparting quality education and was graded B++ by NAAC in 2004 and was awarded College with Potential for Excellence (CPE) status by the UGC in 2005. Ewing Christian College, Allahabad has fully implemented its plan to convert into an e-learning hub. Our e-library is fully equipped with 130 computers and all necessary accessories. It is operational with optical fiber connectivity extended up to classrooms in all the departments.

With University of Allahabad becoming a Central University in 2005, and Ewing Christian College being allowed by the University and UGC to function as an autonomous constituent college of Allahabad Central University, decks are now cleared for starting of Post Graduate classes in the near future.

The College has submitted proposals of the Departments of Botany, Zoology, Physics, Chemistry and Computer Application for “Star College Scheme for Strengthening of Life Sciences & Biotechnology Education & Training at Undergraduate Level”.

